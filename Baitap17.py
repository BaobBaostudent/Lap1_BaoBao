a = int(input("Nhập số thứ nhất: "))
b = int(input("Nhập số thứ hai: "))
c = int(input("Nhập số thứ ba: "))
SLN = max(a, b, c )
SNN = min(a, b, c )
print("Số lớn nhất là: ", SLN)
print("Số nhỏ nhất là: ", SNN)